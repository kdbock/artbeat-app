import 'package:flutter/material.dart';

/// Comprehensive full reaction picker with categorized emoji sections
class FullReactionPicker extends StatefulWidget {
  final void Function(String) onReactionSelected;
  final VoidCallback onClose;

  const FullReactionPicker({
    super.key,
    required this.onReactionSelected,
    required this.onClose,
  });

  @override
  State<FullReactionPicker> createState() => _FullReactionPickerState();
}

class _FullReactionPickerState extends State<FullReactionPicker>
    with TickerProviderStateMixin {
  late TabController _tabController;
  final TextEditingController _searchController = TextEditingController();
